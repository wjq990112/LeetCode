/**
 * @file 开始之前: 搜索二维矩阵 II
 */
import searchMatrix from '../03.search_matrix';

describe('开始之前: 搜索二维矩阵 II', () => {
  it('input: [[-5]], 10', () => {
    expect(searchMatrix([[-5]], 10)).toBeFalsy();
  });

  it(`input: [
               [1,   4,  7, 11, 15],
               [2,   5,  8, 12, 19],
               [3,   6,  9, 16, 22],
               [10, 13, 14, 17, 24],
               [18, 21, 23, 26, 30]
             ], 5`, () => {
    expect(
      searchMatrix(
        [
          [1, 4, 7, 11, 15],
          [2, 5, 8, 12, 19],
          [3, 6, 9, 16, 22],
          [10, 13, 14, 17, 24],
          [18, 21, 23, 26, 30]
        ],
        5
      )
    ).toBeTruthy();
  });

  it(`input: [
               [1,   4,  7, 11, 15],
               [2,   5,  8, 12, 19],
               [3,   6,  9, 16, 22],
               [10, 13, 14, 17, 24],
               [18, 21, 23, 26, 30]
             ], 20`, () => {
    expect(
      searchMatrix(
        [
          [1, 4, 7, 11, 15],
          [2, 5, 8, 12, 19],
          [3, 6, 9, 16, 22],
          [10, 13, 14, 17, 24],
          [18, 21, 23, 26, 30]
        ],
        20
      )
    ).toBeFalsy();
  });
});
